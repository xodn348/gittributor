const TEXT_EXTENSIONS = [".md", ".txt", ".rst", ".adoc", ".asciidoc"];

export function detectTypos(
  content: string,
  filePath: string,
): ContributionOpportunity[] {
  const extension = filePath.slice(filePath.lastIndexOf("."));
  if (!TEXT_EXTENSIONS.includes(extension)) {
    return [];
  }

  const opportunities: ContributionOpportunity[] = [];
  const words = content.split(/\s+/);

  for (const word of words) {
    const cleanWord = word.toLowerCase().replace(/[^a-z]/g, "");
    if (COMMON_MISSPELLINGS[cleanWord]) {
      const correction = COMMON_MISSPELLINGS[cleanWord];
      const wordStart = content.indexOf(word);
      const original = content.substring(wordStart, wordStart + word.length);

      opportunities.push({
        repo: {
          owner: "",
          name: "",
          fullName: "",
          stars: 0,
          language: null,
          description: null,
          isArchived: false,
          defaultBranch: "main",
          hasContributing: false,
          topics: [],
          openIssues: 0,
        },
        type: "typo",
        filePath,
        description: `Fix typo: "${original}" -> "${correction}"`,
        original: original,
        replacement: correction,
        mergeProbability: { score: 0.85, label: "high", reasons: ["Common typo fix"] },
        detectedAt: new Date().toISOString(),
      });
    }
  }

  return opportunities;
}

export function createTypoFix(
  opportunity: ContributionOpportunity,
): { patch: string; description: string; confidence: number } {
  const correction = opportunity.replacement ?? "";

  return {
    patch: correction,
    description: `Fixed typo: "${opportunity.original}" -> "${correction}"`,
    confidence: 0.9,
  };
}